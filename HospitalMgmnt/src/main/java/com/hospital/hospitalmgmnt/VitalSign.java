public class VitalSign {
    private int bloodPr;
    private int hrtRate;
    private int temp;
    private Double weight;
    
    public int getBloodPr() {
        return bloodPr;
    }

    public void setBloodPr(int bloodPr) {
        this.bloodPr = bloodPr;
    }

    public int getHrtRate() {
        return hrtRate;
    }

    public void setHrtRate(int hrtRate) {
        this.hrtRate = hrtRate;
    }

    public int getTemp() {
        return temp;
    }

    public void setTemp(int temp) {
        this.temp = temp;
    }

    public Double getWeight() {
        return weight;
    }

    public void setWeight(Double weight) {
        this.weight = weight;
    }
       
}
