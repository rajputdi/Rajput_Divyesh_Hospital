        model.setValueAt(per.getCommunity_name(), selectedRowIndex, 4);
        model.setValueAt(per.getCityName(), selectedRowIndex, 5);
        
        JOptionPane.showMessageDialog(this, "Record Updated Successfully!!!");  
        
